public class GeometricObj
{
    private String color;
    private boolean isFilled;

    public String getColor()
    {
        return color;
    }

    public void setColor(String c)
    {
        this.color = c;
    }

    public void setFill(boolean fill)
    {
        isFilled = fill;
    }

    public boolean isFilled()
    {
        return isFilled;
    }
}
